export const WEBGL_PRESET_OPTIONS = Object.fromEntries(
  WEBGL_PRESETS.map((preset) => [preset.name, preset.id])
) as Record<string, string>;

export function getWebglPresetById(presetId: string): SpiroWebglPreset {
  return WEBGL_PRESETS.find((preset) => preset.id === presetId) ?? WEBGL_PRESETS[0];
}
