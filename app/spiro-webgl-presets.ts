import type { RenderControlValues } from './spiro-types';

export interface SpiroWebglPreset {
  id: string;
  name: string;
  description: string;
  render: Partial<RenderControlValues>;
}

export const DEFAULT_WEBGL_PRESET_ID = 'blue-gray-hologram-ring';

function webglPreset(
  id: string,
  name: string,
  description: string,
  render: Partial<RenderControlValues>
): SpiroWebglPreset {
  return {
    id,
    name,
    description,
    render: {
      ...render,
      webglPreset: id,
    },
  };
}

